package org.mycore.vidconv.frontend.entity;

import java.util.List;

import jakarta.xml.bind.annotation.XmlRootElement;

/**
 * @author Ren\u00E9 Adler (eagle)
 *
 */
@XmlRootElement(name = "decoder")
public class DecoderWrapper {

    private String name;

    private String description;

    private List<ParameterWrapper> parameters;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the parameters
     */
    public List<ParameterWrapper> getParameters() {
        return parameters;
    }

    /**
     * @param parameters the parameters to set
     */
    public void setParameters(List<ParameterWrapper> parameters) {
        this.parameters = parameters.isEmpty() ? null : parameters;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString() {
        final int maxLen = 10;
        StringBuilder builder = new StringBuilder();
        builder.append("DecoderWrapper [");
        if (name != null) {
            builder.append("name=");
            builder.append(name);
            builder.append(", ");
        }
        if (description != null) {
            builder.append("description=");
            builder.append(description);
            builder.append(", ");
        }
        if (parameters != null) {
            builder.append("parameters=");
            builder.append(parameters.subList(0, Math.min(parameters.size(), maxLen)));
        }
        builder.append("]");
        return builder.toString();
    }

}
